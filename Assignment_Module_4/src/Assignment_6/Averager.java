package Assignment_6;

public class Averager {

	static double avg(int a, int b) {

		double sum = a + b;

		return sum / 2;

	}

	static double avg(int a, int b, int c) {

		double sum = a + b + c;

		return sum / 3;

	}
	
	static double avg(double a, double b) {

		double sum = a + b;

		return sum / 2;

	}

}
